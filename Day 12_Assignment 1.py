'''1. Write a Python program to find the number of times 4 appears in the tuple. 
      Input: tuplex = (2, 4, 5, 6, 2, 3, 4, 4, 7 )'''

# Input tuple
tuplex = (2, 4, 5, 6, 2, 3, 4, 4, 7)

# Count the number of times 4 appears in the tuple
count_4 = tuplex.count(4)

# Output the result
print(count_4)

'''Output: 3 '''

 
