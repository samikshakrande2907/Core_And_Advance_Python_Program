# Declare two variables and print that which variable is largest using ternary operators
# Declare two variables
num1 = 10
num2 = 20

# Use ternary operator to find the largest number
largest = num1 if num1 > num2 else num2

# Print the result
print("The largest number between", num1, "and", num2, "is:", largest)

#output

"The largest number between 10 and 20 is: 20"
