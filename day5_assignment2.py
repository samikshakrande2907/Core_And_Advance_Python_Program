#. Declare a square() function with one parameter.Then call the function and pass one number and display the square of that number .
# Define the square() function with one parameter
def square(num):
    # Calculate the square of the number and return the result
    return num * num

# Call the function with a number
result = square(5)

# Display the result
print("The square of the number is:", result)


'''output
The square of the number is: 25'''
